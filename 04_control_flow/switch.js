const month = "Feb"

switch (month) {
    case "Jan":
        console.log("January");
        break;

    case "Feb":
        console.log("Feb");
        break;

    case "march":
        console.log("March");
        break;

    case "april":
        console.log("April");
        break;

    default:
        break;
}